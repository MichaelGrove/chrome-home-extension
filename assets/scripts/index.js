window.addEventListener('DOMContentLoaded', function() {
	function HomePage() {
		this.init = function() {
			this.GreetingEl = document.getElementById('Greeting');
			this.CurrentTimeEl = document.getElementById('CurrentTime');
			this.QuoteEl = document.getElementById('Quote');

			this.setTime();
			this.setGreeting();
			this.setRandomQuote();
			return this;
		}

		this.setTime = function() {
			this.CurrentTimeEl.innerText = moment().format('HH:mm');
		}
	
		this.setGreeting = function() {
			var text = "Hello";
			var hours = moment().hours()
			if (hours > 4 && hours < 12) {
				text = "Good morning";
			} else if (hours >= 12 && hours < 18) {
				text = "Good afternoon";
			} else {
				text = "Good evening";
			}
			this.GreetingEl.innerText = text;
		}

		this.setRandomQuote = function () {
			this.QuoteEl.innerText = quotes[Math.floor(Math.random() * quotes.length)];
		}

		return this.init();
	}

	var homePage = new HomePage();
	setInterval(function() {
		homePage.setTime();
	}, 1000);
})
